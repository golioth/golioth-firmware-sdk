#include "golioth_sys.h"
#include <FreeRTOS.h>
#include <task.h>

/*--------------------------------------------------
 * Time
 *------------------------------------------------*/
uint64_t golioth_sys_now_ms() {
    return xTaskGetTickCount() * portTICK_PERIOD_MS;
}

void golioth_sys_msleep(uint32_t ms) {
    vTaskDelay(ms / portTICK_PERIOD_MS);
}

